/**
 * Scheduler
 * ===
 *
 * @module engine.Scheduler
 */

////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////
import EventQueue from './event-queue';

////////////////////////////////////////////////////////////////////////////////
// Definitions
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Class
////////////////////////////////////////////////////////////////////////////////
/**
 * Scheduler
 * @class
 */
class Scheduler {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * @private
   * @type {EventQueue}
   */
  _eventQueue;

  /**
   * @private
   * @type {array}
   */
  _repeatQueue;

  /**
   * @private
   * @type {object}
   */
  _currentEvent;

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Get _eventQueue.time
   * @public
   * @readonly
   *
   * @return {number}
   */
  get time() {
    return this._eventQueue.time;
  }

  /**
   * Scheduler
   * @constructor
   */
  constructor() {
    this._eventQueue = EventQueue.createInstance();
    this._repeatQueue = [];
    this._currentEvent = null;
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  clear() {
    this._eventQueue.clear();
    this._repeatQueue = [];
    this._currentEvent = null;
  }

  /**
   * Dequeues an item from the scheduler.
   * @return {object}
   */
  dequeue() {
    const EVENT = this._eventQueue.dequeue();

    if (this._repeatQueue.indexOf(EVENT) >= 0) {
      this._eventQueue.enqueue(EVENT);
    }
    return EVENT;
  }

  /**
   * Enqueues an event in the event queue
   * @param {object} event
   * @param {boolean} repeat
   */
  enqueue(event, time, repeat) {
    this._eventQueue.enqueue(event, time);
    if (repeat) {
      this._repeatQueue.push(event);
    }
  }

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Static factory method.
   * @static
   *
   * @return {Scheduler} - A new scheduler
   * instance.
   */
  static createInstance() {
    return new Scheduler();
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default Scheduler;
