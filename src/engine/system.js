/**
 * System
 * @interface
 */
class System {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * The logger for the entity manager.
   * @private
   * @type {Logger}
   */
  _logger;

  /**
   * @private
   * @type {MessageService}
   */
  _messageService;

  /**
   * The assemblage key for the system.
   * @private
   * @type {number}
   */
  _key;

  /**
   * A collection of assemblages used by the system.
   * @protected
   * @type {array}
   */
  _assemblages;

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Get _key
   * @public
   * @readonly
   * @return {number}
   */
  get key() {
    return this._key;
  }

  /**
   * System
   * @constructor
   * @param {LogService} logService - The log service for the simulation.
   * @param {MessageService} messageService - The message service for the simulation.
   * @param {number} key - The assemblage key for the system.
   */
  constructor(logService, messageService, key) {
    this._logger = logService.registerLogger(this.constructor.name);
    this._messageService = messageService;
    this._key = key;

  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Update routine for the system.
   * @public
   * @abstract
   * @param {number} delta - The time elapsed since the last update.
   */
  update(delta) {
    throw Error(`Error: Update called from System base class`);
  }

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////

}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default System;
