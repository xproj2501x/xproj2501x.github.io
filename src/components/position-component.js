/**
 * PositionComponent
 * @class
 * @extends Component
 */
class PositionComponent extends Component {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  /**
   * PositionComponent
   * @constructor
   * @param {number} id - The id of the parent entity.
   * @param {number} type - The type of the PositionComponent.
   * @param {object} state - The state of the PositionComponent.
   */
  constructor(id, type, state) {
    super(id, type, state)

  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////

  /**
   * Static factory method.
   * @static
   * @param {number} id - The id of the parent entity.
   * @param {number} type - The type of the PositionComponent.
   * @param {object} template - The template for the PositionComponent.
   * @param {object} state - The state of the PositionComponent.
   *
   * @throws {InvalidComponentState}
   * @return {PositionComponent} A new PositionComponent instance.
   */
  static createInstance(id, type, template, state) {
    for (const KEY in state) {
      if (state.hasOwnProperty(KEY)) {
        if (!template.hasOwnProperty(KEY)) {
          throw new InvalidComponentState(`Error: Property ${KEY} is not valid for PositionComponent type ${type}.`);
        }
        if (typeof state[KEY] !== template[KEY]) {
          throw new InvalidComponentState(
            `Error: Property type ${typeof state[KEY]} is not valid for property ${KEY} of PositionComponent type ${type}.`);
        }
      }
    }
    return new PositionComponent(id, type, state);
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default PositionComponent;
