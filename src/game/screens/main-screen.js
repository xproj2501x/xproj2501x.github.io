////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////
import Screen from '../../user-interface/screen';
import Command from '../../common/services/message/command';

////////////////////////////////////////////////////////////////////////////////
// Definitions
////////////////////////////////////////////////////////////////////////////////
const OPTIONS = {
  width: 80,
  height: 25,
  fontSize: 15,
  fontFamily: 'monospace'
};

const KEY = {
  UP: 'w',
  DOWN: 's',
  LEFT: 'a',
  RIGHT: 'd'
};
////////////////////////////////////////////////////////////////////////////////
// Class
////////////////////////////////////////////////////////////////////////////////
/**
 * MainScreen
 * @class
 * @extends Screen
 */
class MainScreen extends Screen {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * MainScreen
   * @constructor
   * @param {string} id - The id of the screen.
   * @param {HTMLCanvasElement} canvas - The canvas element for the screen.
   */
  constructor(id, canvas) {
    super(id, canvas);
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  handleInput(input) {
    let command;

    switch (input) {
      case KEY.UP:
        command = Command.createInstance('INPUT_COMMAND', {action: 'MOVE', direction: [0, -1], speed: 1});
        break;
      case KEY.DOWN:
        command = Command.createInstance('INPUT_COMMAND', {action: 'MOVE', direction: [0, 1], speed: 1});
        break;
      case KEY.LEFT:
        command = Command.createInstance('INPUT_COMMAND', {action: 'MOVE', direction: [-1, 0], speed: 1});
        break;
      case KEY.RIGHT:
        command = Command.createInstance('INPUT_COMMAND', {action: 'MOVE', direction: [1, 0], speed: 1});
        break;
      default:
        break;
    }
    return command;
  }
  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////
  _refresh() {

  }
  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Static factory method.
   * @static
   * @param {string} id - The id of the screen.
   * @param {HTMLElement} container - The HTML container for the screen.
   *
   * @return {MainScreen} A new screen instance.
   */
  static createInstance(id, container) {
    const CANVAS = document.createElement('canvas');

    container.appendChild(CANVAS);
    return new MainScreen(id, CANVAS);
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default MainScreen;
