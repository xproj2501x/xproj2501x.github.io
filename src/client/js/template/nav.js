////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////
import React from 'react';
import {Link} from 'react-router-dom';

////////////////////////////////////////////////////////////////////////////////
// Class
////////////////////////////////////////////////////////////////////////////////
/**
 * Nav
 * @class
 */
class Nav extends React.Component {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  /**
   * Nav
   * @constructor
   */
  constructor(props) {
    super(props);
    this.state = {
      collapsed: props.collapsed
    };
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  componentWillReceiveProps(newProps) {
    if (this.state.collapsed !== newProps.collapsed) {
      this.setState({collapsed: newProps.collapsed});
    }
  }

  /**
   *
   * @return {*[]}
   */
  render() {
    return (
      <div className={this.state.collapsed ? 'o-nav o-nav--collapsed' : 'o-nav'}>
        <ul className="o-nav__menu">
          <li><Link to='/game-of-life' className="o-nav__menu__item">Game of Life</Link></li>
          <li><Link to='/stardew-valley' className="o-nav__menu__item">Stardew Valley</Link></li>
        </ul>
      </div>
    );
  }

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////

}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default Nav;
