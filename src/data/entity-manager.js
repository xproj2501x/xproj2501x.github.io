/**
 * EntityManager
 * ===
 *
 * @module entityManager
 */

////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////
import {EntityNotFound} from './exceptions';
import Entity from './entity';
import UUID from '../common/utilities/uuid';

////////////////////////////////////////////////////////////////////////////////
// Definitions
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Class
////////////////////////////////////////////////////////////////////////////////
/**
 * EntityManager
 * @class
 */
class EntityManager {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * The logger for the entity manager.
   * @private
   * @type {Logger}
   */
  _logger;

  /**
   * A collection of generated entities.
   * @private
   * @type {object}
   */
  _entities;

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  /**
   * EntityManager
   * @constructor
   * @param {LogService} logService - The log service for the simulation.
   */
  constructor(logService) {
    this._logger = logService.registerLogger(this.constructor.name);
    this._entities = {};
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Creates a new entity.
   * @public
   *
   * @return {string} The id of the new entity.
   */
  createEntity() {
    const ID = UUID.create();
    const ENTITY = Entity.createInstance(ID);

    this._entities[ID] = ENTITY;
    return ID;
  }

  /**
   * Destroys an entity with a matching id.
   * @public
   * @param {string} id - The id of the entity.
   *
   * @throws {EntityNotFound}
   */
  destroyEntity(id) {
    if (!this._entities[id]) throw new EntityNotFound(`Error: Entity id ${id} does not exist.`);
    this._entities[id] = null;
  }

  /**
   * Finds an entity with a matching id.
   * @public
   * @param {string} id - The id of the entity.
   *
   * @return {boolean}
   */
  findEntity(id) {
    if (!this._entities[id]) throw new EntityNotFound(`Error: Entity id ${id} does not exist.`);
    return this._entities[id];
  }

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Static factory method.
   * @static
   * @param {LogService} logService - The log service for the simulation.
   *
   * @return {EntityManager} - A new entity manager instance.
   */
  static createInstance(logService) {
    return new EntityManager(logService);
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default EntityManager;
